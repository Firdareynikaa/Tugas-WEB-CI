	<p style="margin-top: 40px;">PT Dyandra Promosindo kembali gelar pameran otomotif Indonesia International Motor Show (IIMS) 2018 pada 19-29 April 2018. Berlokasi di JIExpo Kemayoran, Jakarta, IIMS bakal diselenggarakan selama 11 hari. Memastikan kesiapan dan koordinasi dengan para exhibitor, Dyandra selenggarakan technical meeting IIMS 2018. Pebicaraan mengarah pada kegiatan teknis pada waktu set-up, showdays, maupun breakdown setelah pameran, demi kelancaran acara.</p>

